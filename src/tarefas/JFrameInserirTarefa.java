    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        try {
            //Inicializacao das variaveis
            String descricao = txtDescricao.getText();
            double valor = Double.parseDouble(txtValor.getText());
            String nome = cbxClientes.getSelectedItem().toString();
            String dataInicio = datePicker1.getText();
            String dataFim = datePicker2.getText();
            String tempoInicio = timePicker1.getTimeStringOrEmptyString();
            String tempoFim = timePicker2.getTimeStringOrEmptyString();
            
            String dateTimeInicio = dataInicio + " " + tempoInicio;
            String dateTimeFim = dataFim + " " + tempoFim;
            
            //Buscar cartao cidadao do cliente
            int pk = getCartaoCidadaoDeUmCliente(nome);
            //Buscar opcao selecionada
            String selecao = getOpcaoSelecionada();
            //Inserir uma nova Tarefa no cliente com aquele cartao cidadao
            String sqlCodes = "INSERT INTO tarefas(cliente_cc, descricao, data_inicio, data_fim,valor, estado) VALUES("
                    + pk + ",'"
                    + descricao + "','"
                    + dateTimeInicio + "','"
                    + dateTimeFim + "','"
                    + valor + "','"
                    + selecao + "');";

            database.executeUpdate(sqlCodes);
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(JFrameInserirTarefa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private int getCartaoCidadaoDeUmCliente(String nome) {
        try {
            String sqlCodes = "SELECT cartao_cidadao FROM clientes WHERE nome = '" + nome + "';";
            ResultSet resultadoClientes = database.executeQuery(sqlCodes);
            
            int primaryKey = 0;
            if(resultadoClientes.next()) {
                primaryKey = resultadoClientes.getInt("cartao_cidadao");
            }
            
            return primaryKey;
        } catch (SQLException ex) {
            Logger.getLogger(JFrameInserirTarefa.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 0;
    }
    
    private String getOpcaoSelecionada() {
        String selecao = "";
        int qualSelecionado = 0;
        if(rdbAbandonado.isSelected()) {
            //selecao = "abandonado";
            qualSelecionado = 1;
        }else if(rdbTerminado.isSelected()) {
            //selecao = "terminado";
            qualSelecionado = 2;
        }else if(rdbDesenvolver.isSelected()) {
            //selecao = "desenvolver";
            qualSelecionado = 3;
        }

        selecao = (qualSelecionado == 1) ? "abandonado" : (qualSelecionado == 2) ? "terminado" : "desenvolver";
        
        return selecao;
    }
